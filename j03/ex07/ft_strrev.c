char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	temp;

	i = 0;
	j = 0;
	while (str[i] != '\0')
		i++;
	while (j < i)
	{
		temp = str[i - 1];
		str[i - 1] = str[j];
		str[j] = temp;
		i--;
		j++;
	}
	return (str);
}
